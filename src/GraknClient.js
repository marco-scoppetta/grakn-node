const grpc = require("grpc");
const GraknTx = require("./GraknTx");
const services = require("./autogenerated/grakn_grpc_pb");

function GraknClient(uri, keyspace, credentials) {
  this.client = new services.GraknClient(
    uri,
    grpc.credentials.createInsecure()
  );
  this.keyspace = keyspace;
  this.credentials = credentials;
  this.tx = new GraknTx(this.client, this.keyspace, this.credentials);
}

GraknClient.prototype.execute = async function execute(query) {
  try {
    if (!this.tx.isOpen()) {
      await this.tx.open();
    }
    return await this.tx.execute(query);
  } catch (e) {
    throw e;
  }
};

GraknClient.prototype.open = async function () {
  try {
    await this.tx.open();
    return this.tx;
  } catch (e) {
    throw e;
  }
}

module.exports = GraknClient;
